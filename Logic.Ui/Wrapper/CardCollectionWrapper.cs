
using System.Collections.ObjectModel;
using De.HsFlensburg.LernkartenApp001.Business.Model.BusinessObjects;

namespace De.HsFlensburg.LernkartenApp001.Logic.Ui.Wrapper
{
    public class CardCollectionWrapper : ObservableCollection<CardWrapper>
    {
        public CardCollectionWrapper()
        {
            cardCollection = new CardCollection();
        }
        public CardCollection cardCollection;

        public Card Card
        {
            get
            {
                return cardCollection.Card;
            }

            set
            {
                cardCollection.Card = value;
            }
        }

        public void addCard()
        {
            cardCollection.addCard() ;
        }
    }
}
